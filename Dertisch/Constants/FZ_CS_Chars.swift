// set of popular chars, created so as to ensure that an ampersand (say)
// in one class is the same as an ampersand in another
public struct FZCharConsts {
	public static let
	ampersand =					"&",
	asterisk =					"*",
	at =						"@",
	backSlash =					"\\",
	caret =						"^",
	closedCurlyBracket =		"}",
	closedParenthesis =			")",
	closedSquareBracket =		"]",
	colon =						":",
	comma =						",",
	dollarSign =				"$",
	dot =						".",
	doubleQuote =				"\"",
	ellipsis =					"…",
	emptyString =				"",
	equals =					"=",
	exclamationMark =			"!",
	graveAccent =				"`",
	greaterThan =				">",
	hash =						"#",
	hyphen =					"-",
	lessThan =					"<",
	lineBreak =					"\n",
	minus =						hyphen,
	openCurlyBracket =			"{",
	openParenthesis =			"(",
	openSquareBracket =			"[",
	percent =					"%",
	plus =						"+",
	plusMinus =					"±",
	point =						dot,
	poundSign =					"£",
	questionMark =				"?",
	sectionSign =				"§",
	semicolon =					";",
	singleQuote =				"'",
	slash =						"/",
	space =						" ",
	tilda =						"~",
	underscore =				"_",
	verticalBar =				"|"
}
